<h1>Spelregels</h1>
<p>
  Rummikub is een gezelschapsspel dat speelbaar is met één of meerdere spelers.
  Het doel van het spel is om zo snel mogelijk de steentjes op je plank te
  spelen op de tafel. Je kunt alleen zelf zien welke steentjes je op je plank
  hebt.
</p>
<p>
  Een speler begint met 14 stenen. Elke steen heeft een cijfer van 1 t/m 13 en
  een kleur. Daarnaast zijn er twee jokers die een cijfer en een kleur naar
  keuze van de speler mogen dragen.
</p>
<p>
  Een speler die aan de beurt is mag één van twee dingen doen. Hij pakt een
  steentje uit de pot, of hij voegt één of meer stenen toe aan de tafel. Als een
  speler aan het einde van zijn beurt geen stenen op zijn plank heeft, dan heeft
  hij gewonnen en wordt de score opgemaakt. Heeft hij wel stenen over, dan geeft
  hij de beurt door aan de volgende speler.
</p>
<p>
  Als een speler stenen toevoegt aan de tafel, dan mag hij de stenen op tafel
  opnieuw arrangeren. Aan het einde van zijn beurt moeten alle stenen zich in
  een valide set bevinden.
</p>
<p>
  Een valide set stenen op tafel is een sequentie van drie of meer stenen die
  een valide serie óf een valide rij vormen. Een valide serie bestaat uit een
  sequentie van oplopende getallen in dezelfde kleur zonder tussenliggende
  getallen. Een valide rij is een sequentie van stenen in elk verschillende
  kleuren met hetzelfde getal; de volgorde van de stenen maakt hier niet uit.
</p>
<h2>Bijzonderheden</h2>
<p>
  Als een speler nog niet eerder stenen heeft toegevoegd aan het bord, dan mag
  deze pas spelen als de som van de cijfers op nieuw toegevoegde stenen minimaal
  dertig bedraagt. Een speler mag hier ook nog niet reeds interacteren met reeds
  aangelegde stenen.
</p>
<p>
  Aan het einde van het spel wordt de score geteld door de som te nemen van de
  cijfers op alle resterende stenen van niet-winnende spelers. Iedere
  niet-winnende speler krijgt strafpunten gelijk aan deze som. De winnaar krijgt
  punten gelijk aan de som van de strafpunten van andere spelers. Een
  ongespeelde joker telt hierbij voor 30 punten.
</p>
